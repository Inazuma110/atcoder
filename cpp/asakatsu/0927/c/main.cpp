typedef long long ll;
typedef pair<ll, ll> p;


int main(){
  ios::sync_with_stdio(false);
  cin.tie(0);
  string s;
  cin >> s;
  auto n = s.size();
  ll res = (n-1)*2;
  for (int i = 1; i < n-1; ++i) {
    ll down = i;
    ll up = n-i-1;
    if(s[i] == 'U'){
      res += up + 2*down;
    }else{
      res += 2*up + down;
    }
  }

  cout << res << endl;
}
