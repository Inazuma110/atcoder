typedef long long ll;
typedef pair<ll, ll> p;
map< int64_t, int > prime_factor(int64_t n) {
  map< int64_t, int > ret;
  for(int64_t i = 2; i * i <= n; i++) {
    while(n % i == 0) {
      ret[i]++;
      n /= i;
    }
  }
  if(n != 1) ret[n] = 1;
  return ret;
}

int main(){
  ios::sync_with_stdio(false);
  cin.tie(0);
  ll n;
  cin >> n;
  auto v = prime_factor(n);
  ll res = 0;
  for(auto a : v){
    int count = 1;
    while(a.second >= count){
      res++;
      a.second -= count;
      count++;
    }
  }
  cout << res << endl;
}
